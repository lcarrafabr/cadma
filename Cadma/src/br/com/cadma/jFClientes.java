    public void getGradeSituacao() throws ParseException{
       //Consultar pedidos
        Clientes oCliente = new Clientes();
        if ( nomejTextField.getText().length() > 0 ){
            oCliente.setNome(nomejTextField.getText()+"%");
        }



        ResultSet resultSet = null;

        String[] colunasTabela = new String[]{"Código", "Cliente", "Endereço", "Telefone"};
        DefaultTableModel modeloTabela = new DefaultTableModel(null, colunasTabela) {

            @Override
            public boolean isCellEditable(int row, int col) {
                return false;
            }
        };
        gradeClientesjTable.setModel(modeloTabela);
        gradeClientesjTable.getColumnModel().getColumn(0).setPreferredWidth(30);
        gradeClientesjTable.getColumnModel().getColumn(1).setPreferredWidth(200);
        gradeClientesjTable.getColumnModel().getColumn(2).setPreferredWidth(250);
        gradeClientesjTable.getColumnModel().getColumn(3).setPreferredWidth(70);


try {
            resultSet = oCliente.getConsultar(conexao);

        } catch (SQLException ex) {
            Logger.getLogger(jFProdutos.class.getName()).log(Level.SEVERE, null, ex);
        }
        try {

            while (resultSet.next()) {

                modeloTabela.addRow(new String[]{
                    Funcoes.getCodigoFormat(resultSet.getInt("codCliente")),
                    resultSet.getString("nome"),
                    resultSet.getString("endereco"),
                    resultSet.getString("telefone")
                });

            }

        } catch (SQLException ex) {
            Logger.getLogger(jFClientes.class.getName()).log(Level.SEVERE, null, ex);
        }

           }

    private void getField() throws SQLException{
        Clientes oCliente = new Clientes();
        oCliente.setCodCliente(codCliente);

        ResultSet rs = null;
        rs = oCliente.getConsultar(conexao);

        rs.first();


        codigojSpinner.setValue(rs.getInt("codCliente"));
        nomejTextField.setText(rs.getString("nome"));
        tipoClientejComboBox.setSelectedItem(rs.getString("tipoCliente"));
        cpfCnpjFormattedTextField.setText(rs.getString("cpfCnpj"));
        rgInscricaoEstadualjTextField.setText(rs.getString("rgInsEstadual"));
        enderecojTextField.setText(rs.getString("endereco"));
        numerojTextField.setText(rs.getString("numero"));
        bairrojTextField.setText(rs.getString("bairro"));
        complementojTextField.setText(rs.getString("complemento"));
        cidadejTextField.setText(rs.getString("cidade"));
        ufjComboBox.setSelectedItem(rs.getString("uf"));
        telefonejFormattedTextField.setText(rs.getString("telefone"));
        emailjTextField.setText(rs.getString("email"));






    }

