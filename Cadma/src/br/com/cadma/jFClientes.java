    private void tipoClientejComboBoxItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_tipoClientejComboBoxItemStateChanged
        // TODO add your handling code here:
        

        if (tipoClientejComboBox.getSelectedItem().equals("Física")) {
            try {
                formato = new MaskFormatter("###.###.###-##");
                cpfjLabel.setText("CPF");
                rgjLabel.setText("RG");
            } catch (ParseException ex) {
                Logger.getLogger(jFClientes.class.getName()).log(Level.SEVERE, null, ex);
            }

        }else{
            try {
                formato = new MaskFormatter("##.###.###/####-##");
                cpfjLabel.setText("CNPJ");
                rgjLabel.setText("Insc. Estadual");
            } catch (ParseException ex) {
                Logger.getLogger(jFClientes.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        DefaultFormatterFactory fator = new DefaultFormatterFactory(formato);
        cpfCnpjFormattedTextField.setFormatterFactory(fator);



    }//GEN-LAST:event_tipoClientejComboBoxItemStateChanged

