      public void getGrandeEmpresa() throws ParseException{
       //Consultar pedidos
        Empresa oEmpresa = new Empresa();

        if ( razaosocialjTextField.getText().length() > 0 ){
            oEmpresa.setEmpresa(razaosocialjTextField.getText()+"%");
        }

        ResultSet resultSet = null;

        String[] colunasTabela = new String[]{"Código", "Empresa", "Endereço", "Telefone"};
        DefaultTableModel modeloTabela = new DefaultTableModel(null, colunasTabela) {

            @Override
            public boolean isCellEditable(int row, int col) {
                return false;
            }
        };
        gradeEmpresajTable.setModel(modeloTabela);
        gradeEmpresajTable.getColumnModel().getColumn(0).setPreferredWidth(50);
        gradeEmpresajTable.getColumnModel().getColumn(1).setPreferredWidth(200);
        gradeEmpresajTable.getColumnModel().getColumn(2).setPreferredWidth(300);



try {
            resultSet = oEmpresa.getConsultar(conexao);
            if (resultSet.getRow() == 1){
             resultSet.first();
            }


        } catch (SQLException ex) {
            Logger.getLogger(JEmpresa.class.getName()).log(Level.SEVERE, null, ex);
        }
        try {

            while (resultSet.next()) {

                modeloTabela.addRow(new String[]{
                    Funcoes.getCodigoFormat(resultSet.getInt("codEmpresa")),
                    resultSet.getString("empresa"),
                    resultSet.getString("endereco"),
                    resultSet.getString("telefone"),
                });

            }

        } catch (SQLException ex) {
            Logger.getLogger(JEmpresa.class.getName()).log(Level.SEVERE, null, ex);
        }

           }

    public void getFieldEmpresa(int codEmpresa) throws ParseException{
        
        Empresa oEmpresa = new Empresa();
        ResultSet rs = null;
        oEmpresa.setCodEmpresa(codEmpresa);

        try {
            rs = oEmpresa.getConsultar(conexao);
            rs.next();

            //Pegar campos
            razaosocialjTextField.setText(rs.getString("empresa"));
            codEmpresajSpinner.setValue(rs.getInt("codEmpresa"));
            cnpjFormattedTextField.setText(rs.getString("cnpj"));
            inscEstadualjTextField.setText(rs.getString("insEstadual"));
            enderecojTextField.setText(rs.getString("endereco"));
            bairrojTextField.setText(rs.getString("bairro"));
            cidadejTextField.setText(rs.getString("cidade"));
            numerojTextField.setText(rs.getString("numero"));
            ufjComboBox.setSelectedItem(rs.getString("uf"));
            telefonejFormattedTextField.setText(rs.getString("telefone"));
            emailjTextField.setText(rs.getString("email"));
            homePagejTextField.setText(rs.getString("homePage"));
            inscEstadualjTextField.setText(rs.getString("insEstadual"));

        } catch (SQLException ex) {
            Logger.getLogger(Produtos.class.getName()).log(Level.SEVERE, null, ex);
        }



    }


