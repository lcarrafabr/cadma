    private void salvarjButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_salvarjButtonActionPerformed
        // TODO add your handling code here:
        Empresa oEmpresa = new Empresa();

        if (razaosocialjTextField.getText().equals("") || enderecojTextField.getText().equals("") || numerojTextField.getText().equals("") ||
            bairrojTextField.getText().equals("")) {


            JOptionPane.showMessageDialog(null, "Preencha os campos: Razao Social, Endereço, numero e Bairro.", "ATENÇÃO", JOptionPane.INFORMATION_MESSAGE);
        }else{try {
                {
                    salvarjButton.setEnabled(false);
                    alterarjButton.setEnabled(false);
                    excluirjButton.setEnabled(false);
                    novojButton.setEnabled(true);
                    editarjButton.setEnabled(true);
                    
                    oEmpresa.setEmpresa(razaosocialjTextField.getText());
                    oEmpresa.setCnpj(cnpjFormattedTextField.getText());
                    oEmpresa.setEndereco(enderecojTextField.getText());
                    oEmpresa.setNumero(numerojTextField.getText());
                    oEmpresa.setBairro(bairrojTextField.getText());
                    oEmpresa.setCidade(cidadejTextField.getText());
                    oEmpresa.setUf((String) ufjComboBox.getSelectedItem());
                    oEmpresa.setTelefone(telefonejFormattedTextField.getText());
                    oEmpresa.setEmail(emailjTextField.getText());
                    oEmpresa.setHomePage(homePagejTextField.getText());

                    oEmpresa.setCadastrar(conexao);
                    try {
                        conexao.con.commit();
                        conexao.con.setAutoCommit(true);
                    } catch (SQLException ex) {
                        Logger.getLogger(jFClientes.class.getName()).log(Level.SEVERE, null, ex);
                    }
                    try {
                        getGrandeEmpresa();
                    } catch (ParseException ex) {
                        Logger.getLogger(jFClientes.class.getName()).log(Level.SEVERE, null, ex);
                    }
                }


                Funcoes.limpaCampos(jPanel1);
                razaosocialjTextField.setText("");
                cnpjFormattedTextField.setText("");
                enderecojTextField.setText("");
                numerojTextField.setText("");
                bairrojTextField.setText("");
                homePagejTextField.setText("");
                cidadejTextField.setText("");
                telefonejFormattedTextField.setText("");
                emailjTextField.setText("");
                inscEstadualjTextField.setText("");
                getGrandeEmpresa();
                razaosocialjTextField.grabFocus();
            } catch (ParseException ex) {
                Logger.getLogger(jFClientes.class.getName()).log(Level.SEVERE, null, ex);
            }
        }


       

    }//GEN-LAST:event_salvarjButtonActionPerformed

